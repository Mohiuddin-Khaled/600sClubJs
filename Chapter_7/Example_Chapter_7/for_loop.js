// Loop in JavaScript

// for loop
/* Structure
    for(declare loop variable; loop condition; change loop variable){
        one or more line of code that will be repeated
    }
*/
// log from 0 to 4 in the console
for (let num = 0; num < 5; num++) {
    console.log(num);
}

// log from 1 to 9 in the console
for (let i = 1; i < 10; i++) {
    console.log(i);
}

// log from 50 to 100 in the console
for (let i = 50; i <= 100; i++) {
    console.log(i);
}


// calculate the sum from 11 to 20
let sum = 0;
for (let i = 11; i <= 20; i++) {
    sum = sum + i;
}
console.log('Sum of numbers 11 to 20 is: ', sum);

