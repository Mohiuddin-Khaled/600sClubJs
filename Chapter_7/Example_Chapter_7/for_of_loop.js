// Loop in JavaScript

// for-of loop

/* Structure
   for (variable of iterable)
   {
        / code block
   }
*/

// number element array
const numbers = [12, 98, 45, 63, 21, 72];

for (const num of numbers) {
    console.log(num);
}

// fruit element array
const fruits = ["orange", "apple", "jackfruit", "watermelon"];

for (const item of fruits) {
    console.log(item);
}






