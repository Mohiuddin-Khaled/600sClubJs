/*************************************************************
        ***operators***         ***Description***
               
              +                     Addition
              -                    Subtraction
              *                   Multiplication
             **                 Exponentiation (ES6)                   
              /                     Division
              %                     Modulus (Remainder)
*************************************************************/

const a = 11;
const b = 7;
// Addition
console.log(a + b);
// Subtraction
console.log(a - b);
// Multiplication
console.log(a * b);
// Exponentiation
console.log(a ** b);
// Division
console.log(a / b);
// Modulus(Remainder)
console.log(a % b);