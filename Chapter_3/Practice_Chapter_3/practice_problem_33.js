// string converted to number value
// Use toFixed() to show one digit after point of float number 


const numStr1 = parseFloat('10.5678');
const num1 = parseFloat(numStr1.toFixed(1));
console.log(num1);


const numStr2 = parseFloat('5.4321');
const num2 = parseFloat(numStr2.toFixed(1));
console.log(num2);

