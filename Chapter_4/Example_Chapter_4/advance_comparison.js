// Advance comparison between two values

// two values are same
console.log(10 == 10);

// Number and string values are same
console.log(10 == "10");

// Number and string data types are not same
console.log(10 === "10");

// Number and string types are not same 
console.log(120 === "tomato");

// two values are same
console.log(10 != 10);

// two values are same
console.log(10 != "10");

// two values and data types are not same
console.log(10 !== "10");


